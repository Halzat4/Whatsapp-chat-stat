class TextNormalizer:

	def __init__(self):
		self.morph = pymorphy3.MorphAnalyzer()
		self.stemmer_en = SnowballStemmer('english')
		self.latin_pattern = re.compile(r'[a-z]')
		self.all_stops = TextConfig.get_all_stops()

	def normalize(self, word):
		if not word or word in TextConfig.GLOBAL_JUNK or len(word) > 25:
			return None

		if word in TextConfig.NORM_MAP:
			return TextConfig.NORM_MAP[word]

		if self.latin_pattern.search(word):
			res = self.stemmer_en.stem(word)
		else:
			parse_res = self.morph.parse(word)[0]
			res = parse_res.normal_form
			res = TextConfig.NORM_MAP.get(res, res)

		return res if res and res not in self.all_stops else None


class TextPreprocessor:
	def __init__(self):
		self.normalizer = TextNormalizer()
		self.content_filter = ContentFilter(self.normalizer.morph)

	def parse_line(self, line):
		match = re.search(r'(\d{2}\.\d{2}\.\d{4}), (\d{2}:\d{2}) - (.*?): (.*)', line)
		if match:
			return {
				'date': match.group(1),
				'time': match.group(2),
				'author': match.group(3),
				'text': match.group(4)
			}
		return None

	def clean_text(self, text, strict_filter=False):

		content = TextConfig.LAUGH_PATTERN.sub('хах', text.lower())
		content = re.sub(r'[^а-яА-Яa-zA-Z\s]', ' ', content)

		raw_tokens = content.split()
		clean_tokens = []

		for word in raw_tokens:
			normalized = self.normalizer.normalize(word)
			if not normalized:
				continue

			if strict_filter:
				if self.content_filter.is_junk(word, normalized):
					continue

			for part in normalized.split():
				if part not in self.normalizer.all_stops and len(part) > 1:
					clean_tokens.append(part)

		return clean_tokens


class ContentFilter:
    def __init__(self, morph_analyzer):
        self.morph = morph_analyzer
        self.excluded_pos = {
            'VERB', 'INFN', 'ADJF', 'ADJS', 'COMP',
            'PREP', 'CONJ', 'PRCL', 'INTJ', 'ADVB', 'NPRO'
        }

        self.profanity_labels = {
            'блять', 'мат', 'оскорбление', 'пиздец',
            'нахуй', 'похуй', 'хуй', 'пизда', 'ебать',  'лох', 'изменить', 'удалить'
        }

    def is_junk(self, word, normalized_word):
        if normalized_word in self.profanity_labels:
            return True

        if normalized_word in TextConfig.MODEL_STOPWORDS:
            return True

        parsed = self.morph.parse(word)[0]
        if parsed.tag.POS in self.excluded_pos:
            return True

        return False